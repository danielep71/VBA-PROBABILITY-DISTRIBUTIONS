"""
Independent-oracle cross-check of the benchmark REFERENCE values against SciPy.

The reference values in the extreme Beta/F grids come from a 50-digit
continued-fraction incomplete beta (_ibeta.py). That is high precision but shares
algorithmic structure with the production kernel, so it is not a fully INDEPENDENT
oracle. SciPy (Cephes/Boost lineage) is independent, but loses reliability at very
large shape parameters - exactly the extreme regime the grids probe.

This script therefore cross-checks each reference against SciPy and splits the
points into:
  - cross-checked : SciPy agrees with the reference (within SciPy's ~1E-13 double
                    precision) -> two independent methods concur;
  - beyond SciPy  : the incomplete-beta shape parameter is large enough that SciPy
                    is itself unreliable; these rely on the reference's own
                    50-vs-80-digit self-consistency (reported separately), not on
                    SciPy.

It writes cross_check_scipy.md so the README's "cross-checked against SciPy" claim
is a regenerable artifact, honestly scoped by SciPy's reliable range.
"""
import argparse, csv, glob, os
from decimal import Decimal
import mpmath as mp
from scipy import stats, special
mp.mp.dps = 50

SCIPY_SHAPE_LIMIT = 1e7   # above this incomplete-beta shape param, SciPy is unreliable

def scipy_value(fn, x, a2, a3):
    """Return SciPy's value for the same quantity, or None if out of SciPy's range."""
    try:
        if fn == "Beta_Density":
            return stats.beta.pdf(x, a2, a3)
        if fn == "Beta_Cumulative":
            return special.betainc(a2, a3, x)
        if fn == "Beta_Survival":
            return stats.beta.sf(x, a2, a3)
        if fn == "Beta_InverseCumulative":
            return special.betaincinv(a2, a3, x)   # arg1=p, shapes a2,a3
        if fn == "F_Cumulative":
            return stats.f.cdf(x, a2, a3)
        if fn == "F_Survival":
            return stats.f.sf(x, a2, a3)
        if fn == "F_InverseCumulative":
            return stats.f.ppf(x, a2, a3)
        if fn == "PROB_LogBeta":
            return special.betaln(x, a2)   # arg1=Large, arg2=Small
    except Exception:
        return None
    return None

def beta_shape_param(fn, a2, a3):
    # incomplete-beta shape parameters behind each function
    if fn.startswith("F_"):
        return max(a2, a3) / 2
    return max(a2, a3)

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--grids", nargs="+", required=True, help="reference grids to cross-check")
    ap.add_argument("--out", default="cross_check_scipy.md")
    a = ap.parse_args()

    checked = 0; beyond = 0; worst = 0.0; worst_at = ""
    per_fn = {}
    for path in a.grids:
        for r in csv.DictReader(open(path)):
            fn = r["function"]
            ref_s = r["reference"].strip()
            if not ref_s:
                continue
            try:
                x = float(r["arg1"]); a2 = float(r["arg2"]); a3 = float(r["arg3"]) if r["arg3"].strip() else 0.0
                ref = mp.mpf(ref_s)
            except Exception:
                continue
            bp = beta_shape_param(fn, a2, a3)
            d = per_fn.setdefault(fn, {"checked": 0, "beyond": 0, "worst": 0.0})
            if fn != "PROB_LogBeta" and bp > SCIPY_SHAPE_LIMIT:
                beyond += 1; d["beyond"] += 1
                continue
            sv = scipy_value(fn, x, a2, a3)
            if sv is None or (sv != sv):   # None or NaN
                beyond += 1; d["beyond"] += 1
                continue
            rel = abs(mp.mpf(sv) - ref) / abs(ref) if ref != 0 else abs(mp.mpf(sv) - ref)
            checked += 1; d["checked"] += 1
            if float(rel) > worst:
                worst = float(rel); worst_at = f"{fn} x={r['arg1']} a2={r['arg2'][:8]} a3={r['arg3'][:8]}"
            if float(rel) > d["worst"]:
                d["worst"] = float(rel)

    md = ["# SciPy independent cross-check of benchmark references", "",
          "Regenerated by `cross_check_scipy.py`. Each reference value is compared "
          "against SciPy (an independent Cephes/Boost-lineage implementation) where "
          "SciPy is reliable, i.e. incomplete-beta shape parameter <= "
          f"{SCIPY_SHAPE_LIMIT:.0e}. Points beyond that are outside SciPy's reliable "
          "range and rest on the reference's own 50-vs-80-digit self-consistency.", "",
          "| Function | Cross-checked vs SciPy | Worst rel. diff | Beyond SciPy range |",
          "|---|---:|---:|---:|"]
    for fn in sorted(per_fn):
        d = per_fn[fn]
        wtxt = f"{d['worst']:.1e}" if d["checked"] else "-"
        md.append(f"| {fn} | {d['checked']} | {wtxt} | {d['beyond']} |")
    md += ["", f"> {checked} point(s) independently confirmed by SciPy "
           f"(worst relative difference {worst:.1e}"
           + (f" at {worst_at}" if worst_at else "") + "); "
           f"{beyond} point(s) beyond SciPy's reliable range, covered by "
           "50-vs-80-digit reference self-consistency instead.",
           "",
           "SciPy agreement at this level (double-precision) independently "
           "corroborates the continued-fraction reference wherever SciPy is trustworthy; "
           "it does not, and cannot, certify the extreme-parameter points SciPy itself "
           "cannot compute."]
    with open(a.out, "w", encoding="utf-8") as f:
        f.write("\n".join(md) + "\n")
    print(f"cross-checked {checked} vs SciPy (worst {worst:.1e}); {beyond} beyond SciPy range")
    print(f"wrote {a.out}")

if __name__ == "__main__":
    main()
